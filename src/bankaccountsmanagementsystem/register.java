
package bankaccountsmanagementsystem;


public class register {

    static String name;
    static String city;
    static String age;
    static String password;
    static String typeAcc;
    static String ID;

    register(String name, String city, String age, String password, String typeAcc, String ID) {
        register.name = name;
        register.city = city;
        register.age = age;
        register.password = password;
        register.typeAcc = typeAcc;
        register.ID = ID;
    }
}
